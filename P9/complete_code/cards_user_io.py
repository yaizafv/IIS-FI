def show_cards_range(card_list):
    """Prints all the cards in a list.

    Parameters:
        card_list: The list to print.
    """
    for i in range(len(card_list)):
        print(card_list[i])


def show_cards_foreach(card_list):
    """Prints all the cards in a list.

    Parameters:
        card_list: The list to print.
    """

    for card in card_list:
        print(card)